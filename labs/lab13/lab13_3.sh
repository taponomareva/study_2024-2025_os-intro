#!/bin/bash

# Проверка режима работы: создание или удаление
if [[ "$1" == "delete" ]]; then
  echo "Удаление файлов *.tmp..."
  rm -f *.tmp
  echo "Файлы удалены (если были)."
else
  # Проверка, передано ли число
  if ! [[ "$1" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: укажите количество файлов как положительное число"
    exit 1
  fi

  count=$1
  echo "Создание $count файлов..."
  for ((i=1; i<=count; i++)); do
    touch "$i.tmp"
  done
  echo "$count файлов создано."
fi
